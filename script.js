/* ══════════════════════════════════════════════
   SMOOTH SCROLL
══════════════════════════════════════════════ */
document.querySelectorAll('a[href^="#"]').forEach(a => {
  a.addEventListener('click', e => {
    const target = document.querySelector(a.getAttribute('href'));
    if (target) {
      e.preventDefault();
      target.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
  });
});

/* ══════════════════════════════════════════════
   PROGRESS BAR
══════════════════════════════════════════════ */
const progressBar = document.getElementById('progress-bar');
function updateProgress() {
  const h = document.documentElement;
  const pct = (h.scrollTop / (h.scrollHeight - h.clientHeight)) * 100;
  progressBar.style.width = pct + '%';
}
window.addEventListener('scroll', updateProgress, { passive: true });

/* ══════════════════════════════════════════════
   ACTIVE NAV
══════════════════════════════════════════════ */
const sections   = document.querySelectorAll('section[id]');
const navLinks   = document.querySelectorAll('.nav-links a');
const navEl      = document.getElementById('nav');

function updateNav() {
  const y = window.scrollY + 100;
  navEl.classList.toggle('scrolled', window.scrollY > 20);
  sections.forEach(s => {
    const top = s.offsetTop, h = s.offsetHeight, id = s.id;
    const a = document.querySelector(`.nav-links a[href="#${id}"]`);
    if (a) a.classList.toggle('active', y >= top && y < top + h);
  });
}
window.addEventListener('scroll', updateNav, { passive: true });

/* ══════════════════════════════════════════════
   REVEAL ON SCROLL
══════════════════════════════════════════════ */
const revObs = new IntersectionObserver((entries) => {
  entries.forEach((e, i) => {
    if (e.isIntersecting) {
      setTimeout(() => e.target.classList.add('vis'), i * 60);
      revObs.unobserve(e.target);
    }
  });
}, { threshold: 0.07, rootMargin: '0px 0px -40px 0px' });

document.querySelectorAll('.reveal, .tl-row, .cert-card, .project-row').forEach(el => revObs.observe(el));

