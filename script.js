let lang = 'ca';

function setLang(l) {
  lang = l;
  const t = T[l];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.dataset.i18n;
    if (t[k] !== undefined) el.innerHTML = t[k];
  });
  document.getElementById('lb-ca').classList.toggle('active', l === 'ca');
  document.getElementById('lb-es').classList.toggle('active', l === 'es');
  document.documentElement.lang = l;
  startTyped();
}

/* ══════════════════════════════════════════════
   TYPED EFFECT
══════════════════════════════════════════════ */
const roles = {
  ca: ['Desenvolupador Web', 'CEO de Nuvia Dev', 'Estudiant DAM', 'Entusiasta Ciberseguretat', 'Cambrer a LOKAL'],
  es: ['Desarrollador Web', 'CEO de Nuvia Dev', 'Estudiante DAM', 'Entusiasta Ciberseguridad', 'Camarero en LOKAL'],
};
let ti = 0, ci = 0, deleting = false, typedTimer = null;

function startTyped() {
  ti = 0; ci = 0; deleting = false;
  clearTimeout(typedTimer);
  const el = document.getElementById('typed');
  if (el) el.textContent = '';
  typedStep();
}

function typedStep() {
  const arr = roles[lang];
  const cur = arr[ti];
  const el = document.getElementById('typed');
  if (!el) return;
  if (!deleting) {
    el.textContent = cur.substring(0, ci + 1);
    ci++;
    if (ci === cur.length) { deleting = true; typedTimer = setTimeout(typedStep, 2000); return; }
  } else {
    el.textContent = cur.substring(0, ci - 1);
    ci--;
    if (ci === 0) { deleting = false; ti = (ti + 1) % arr.length; typedTimer = setTimeout(typedStep, 350); return; }
  }
  typedTimer = setTimeout(typedStep, deleting ? 45 : 75);
}

/* ══════════════════════════════════════════════
   CUSTOM CURSOR
══════════════════════════════════════════════ */
const cursorOuter = document.getElementById('cursor');
const cursorDot   = document.getElementById('cursor-dot');
let mx = 0, my = 0, cx = 0, cy = 0;

document.addEventListener('mousemove', e => {
  mx = e.clientX; my = e.clientY;
  cursorDot.style.left = mx + 'px';
  cursorDot.style.top  = my + 'px';
});

function animateCursor() {
  cx += (mx - cx) * 0.12;
  cy += (my - cy) * 0.12;
  cursorOuter.style.left = cx + 'px';
  cursorOuter.style.top  = cy + 'px';
  requestAnimationFrame(animateCursor);
}
animateCursor();

document.querySelectorAll('a, button, [onclick]').forEach(el => {
  el.addEventListener('mouseenter', () => document.body.classList.add('cursor-hover'));
  el.addEventListener('mouseleave', () => document.body.classList.remove('cursor-hover'));
});

